package app;

public class Curso {
	private String id;
	private String nome;
	private String creditos;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getCreditos() {
		return creditos;
	}
	
	public void setCreditos(String creditos) {
		this.creditos = creditos;
	}
}
